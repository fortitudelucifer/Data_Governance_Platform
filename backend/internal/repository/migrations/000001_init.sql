-- =============================================================================
-- assets：sha256 语义（M6，2026-07-14 评审拍板，Phase C/D 前置）
--   · 单文件资产（现状全部）：sha256 = 文件内容哈希，storage_uri 指向该对象。
--   · 多文件资产（Phase C 的 DICOM series / Phase D 的 scene）：storage_uri 指向
--     一份 **manifest 清单对象**，sha256 = manifest 内容哈希（清单里逐文件记 sha）。
--   两种情形下 UNIQUE 与 NOT NULL 语义一致成立；单文件是多文件的退化情形。
--
-- 去重唯一键是**部分唯一索引**（只覆盖 qc_status='passed'）：
--   · 去重逻辑本来就只对 QC 通过的资产生效（QC 失败行是给操作员看的拒收记录，
--     同一个坏文件反复上传、或坏文件与已入库的好文件同内容，都不该撞唯一键）。
--   · 上传路径用 ON CONFLICT (dataset_id, sha256) WHERE qc_status='passed'
--     DO NOTHING → 唯一性成为**数据库的属性**，并发上传同一文件不再插两行
--     （旧的「先查后写」竞态从此关死）。
-- =============================================================================
CREATE TABLE assets (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dataset_id      BIGINT       NOT NULL REFERENCES datasets(id) ON DELETE CASCADE,
    parent_asset_id BIGINT       REFERENCES assets(id) ON DELETE CASCADE,
    modality        VARCHAR(16)  NOT NULL DEFAULT 'image',
    storage_uri     VARCHAR(512) NOT NULL DEFAULT '',
    original_name   VARCHAR(512) NOT NULL DEFAULT '',
    mime            VARCHAR(64)  NOT NULL DEFAULT '',
    sha256          VARCHAR(64)  NOT NULL DEFAULT '',
    size_bytes      BIGINT       NOT NULL DEFAULT 0,
    width           INTEGER      NOT NULL DEFAULT 0,
    height          INTEGER      NOT NULL DEFAULT 0,
    duration_ms     BIGINT,
    fps             DOUBLE PRECISION,
    sample_rate     INTEGER,
    qc_status       VARCHAR(16)  NOT NULL DEFAULT 'pending',
    qc_report       JSONB,
    features        JSONB,
    preprocess_status          VARCHAR(16),
    preprocess_error           TEXT,
    preprocess_attempts        INTEGER NOT NULL DEFAULT 0,
    preprocess_lease_until     timestamptz,
    preprocess_next_attempt_at timestamptz,
    uploader_id     BIGINT       NOT NULL DEFAULT 0,
    created_at      timestamptz  NOT NULL DEFAULT now(),
    updated_at      timestamptz  NOT NULL DEFAULT now()
);

CREATE INDEX idx_assets_dataset_id      ON assets(dataset_id);
CREATE INDEX idx_assets_parent_asset_id ON assets(parent_asset_id);
CREATE INDEX idx_assets_modality        ON assets(modality);
CREATE INDEX idx_assets_sha256          ON assets(sha256);
CREATE INDEX idx_assets_qc_status       ON assets(qc_status);
CREATE INDEX idx_assets_uploader_id     ON assets(uploader_id);
CREATE INDEX idx_assets_preprocess_status ON assets(preprocess_status);
CREATE INDEX idx_assets_preprocess_next   ON assets(preprocess_next_attempt_at);

-- M6：去重唯一键（部分唯一索引，理由见上方大注释）。
CREATE UNIQUE INDEX idx_assets_dataset_sha
    ON assets(dataset_id, sha256) WHERE qc_status = 'passed';

