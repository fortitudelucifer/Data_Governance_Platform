-- documents：文本文档表（DocumentDB 的唯一实现，方案-07 起主部署与 runner
-- 共用）。此前只活在 AutoMigrate 里，从未进过版本化迁移——现在进了。
-- data 是 jsonb（M5/M10），检索走 trigram GIN（与 LOWER(...) LIKE 查询配套）。
CREATE TABLE documents (
    id                       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dataset_id               BIGINT       NOT NULL REFERENCES datasets(id) ON DELETE CASCADE,
    doc_key                  VARCHAR(255) NOT NULL,
    version                  INTEGER      NOT NULL DEFAULT 1,
    is_active                BOOLEAN      NOT NULL DEFAULT TRUE,
    user_id                  BIGINT       NOT NULL DEFAULT 0,
    data                     JSONB,
    annotation_stage         VARCHAR(50)  NOT NULL DEFAULT '',
    refinement_cursor        INTEGER      NOT NULL DEFAULT 0,
    created_by               BIGINT       NOT NULL DEFAULT 0,
    created_at               timestamptz  NOT NULL DEFAULT now(),
    updated_at               timestamptz  NOT NULL DEFAULT now(),
    etag                     VARCHAR(64)  NOT NULL DEFAULT '',
    llm_refinement_enabled   BOOLEAN      NOT NULL DEFAULT FALSE,
    llm_refinement_score     INTEGER,
    llm_refinement_reasoning TEXT,
    llm_refinement_version   VARCHAR(64),
    annotator_user_id        VARCHAR(64),
    annotator_name           VARCHAR(100),
    annotator_action_time    timestamptz
);

CREATE UNIQUE INDEX idx_doc_version    ON documents (dataset_id, doc_key, version);
CREATE INDEX idx_doc_active            ON documents (doc_key, is_active);
CREATE INDEX idx_dataset_active        ON documents (dataset_id, is_active);
CREATE INDEX idx_dataset_updated       ON documents (dataset_id, is_active, updated_at);
CREATE INDEX idx_dataset_stage         ON documents (dataset_id, is_active, annotation_stage);
CREATE INDEX idx_dataset_user          ON documents (dataset_id, user_id);
-- M5：文档搜索（LOWER(doc_key) LIKE / LOWER(data::text) LIKE）的加速索引。
CREATE INDEX idx_documents_dockey_trgm ON documents USING gin (lower(doc_key) gin_trgm_ops);
CREATE INDEX idx_documents_data_trgm   ON documents USING gin (lower(data::text) gin_trgm_ops);

