-- Postgres 目标态 schema（执行方案-06 · P1）。本文件是关系库 schema 的**唯一真源**：
-- AutoMigrate 已删除（M3），dev 与 prod 跑的都是这一份（D1）。
--
-- 这是干净起点：没有存量库要升级，历史上的多文件迁移链已收敛为这一个
-- 建库迁移（含 datasets 的九个看板计数列——内容零丢失）。
--
-- ⚠️ 运维禁令（06 · P-H1 立的，07 载荷入库后仍然有效）：
--   ❌ TRUNCATE ... RESTART IDENTITY      ❌ ALTER SEQUENCE ... RESTART
--   07 之后载荷表与本库同库且 FK 级联，「清一半」类事故已编不出来；但
--   trace_logs 刻意无 FK（可观测性比对象活得久），id 重用会把旧 trace 缝到
--   新任务上。清环境的正路是整库 DROP DATABASE 重建 + Redis db1 flushdb +
--   清 blob 目录，别做表级局部清理。
--
-- 外键策略（M7）：级联只加在**数据脊柱**上（datasets → assets → tasks/derivatives/
-- uploads/documents…），删数据集即级联清光其下的关系行——修掉「删数据集不级联删
-- 资产」的泄漏。**不加指向 users(id) 的外键**：owner_id/user_id/uploader_id 的
-- 「0 = 系统/未指定」是既有写入约定，加 FK 会立刻炸掉现有代码路径；用户删除本就
-- 不该抹掉审计与产出的归属。
--
-- asset_derivatives.kind **刻意不做 CHECK/枚举约束**（06 · §6.2，2026-07-14 评审）：
-- kind 清单随 Phase C/D 扩（dzi_tiles/volume_slices/pcd_tiles/…），做成 DB 枚举
-- = C/D 开工第一天就二次迁移。校验留在应用层（Go 常量）。
-- =============================================================================

